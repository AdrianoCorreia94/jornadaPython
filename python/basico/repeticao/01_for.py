# percorrer com um numero limite determinado

# contado simples
for x in range(10):  # percorre 10 vezes iniciando do zero
    print(x)  # imprime do 0 ao 9, totalizando 10 repeticoes do laco

# contador fatiado
for y in range(4, 11):  # percorre o laco contando do 4 ao 10
    print(y)

# contador com passo
for z in range(0, 20, 3):  # percorre do 0 ao 20 pulando 3 numeros sempre
    print(z)

# contador regressivo
for a in range(13, 0, -1):  # percorre do 13 ao 1 sempre diminuindo 1
    print(a)

# como ja visto, pode-se percorer por tamanho de uma lista